// C Program to print reverse characters

# include <stdio.h>                                                                 // Header file declaration

int main () {                                                                       // main function definition

    char one = 'X';
    char two = 'M';
    char three = 'L';
    printf("\n The reverse of %c%c%c is %c%c%c\n\n",one,two,three,three,two,one);   // Printing reverse 
    return 0;

}                                                                                   // End of main function