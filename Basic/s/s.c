// C Program to find the value of s where s = 1 + 3/2 + 5/4 + 7/8

# include <stdio.h>                                                                         // Header file declaration

int main () {                                                                               // main function definition

    float s = 1 + (float)3/2 + (float)5/4 + (float)7/8;                                     // Variable initialization
    printf("\n s : %0.5f\n\n",s);
    return 0;

}                                                                                           // End of main function